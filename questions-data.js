function normalizeRawText(value) {
  return value.replace(/\r\n/g, "\n").replace(/\u00a0/g, " ");
}

function readNextMeaningful(lines, state) {
  while (state.index < lines.length) {
    const line = lines[state.index].trim();
    state.index += 1;
    if (!line || /^_+$/.test(line) || /^QCM APS/i.test(line)) {
      continue;
    }
    return line;
  }
  return null;
}

function collectExplanation(lines, state) {
  const parts = [];
  while (state.index < lines.length) {
    const line = lines[state.index].trim();
    if (!line) {
      state.index += 1;
      if (parts.length > 0) {
        break;
      }
      continue;
    }
    if (/^_+$/.test(line)) {
      state.index += 1;
      if (parts.length > 0) {
        break;
      }
      continue;
    }
    if (/^Question\s+\d+$/i.test(line)) {
      break;
    }
    parts.push(line.replace(/^•\s*/, "- "));
    state.index += 1;
  }
  return parts.join(" ");
}

function parseMainSection(raw) {
  const lines = normalizeRawText(raw).split("\n");
  const state = { index: 0 };
  const letters = ["A", "B", "C", "D"];
  const questions = [];

  while (state.index < lines.length) {
    const line = readNextMeaningful(lines, state);
    if (!line) {
      break;
    }
    const match = line.match(/^Question\s+(\d+)$/i);
    if (!match) {
      continue;
    }

    const prompt = readNextMeaningful(lines, state);
    const options = letters.map((letter) => {
      const optionLine = readNextMeaningful(lines, state) || "";
      return optionLine.replace(new RegExp(`^${letter}\\.\\s*`), "");
    });

    const answerLine = readNextMeaningful(lines, state) || "";
    const answerMatch = answerLine.match(/^Réponse\s*:\s*([ABCD])$/i);
    const explanationHeader = lines[state.index] && lines[state.index].trim() === "Explication :";
    let explanation = null;

    if (explanationHeader) {
      state.index += 1;
      explanation = collectExplanation(lines, state) || null;
    }

    const answerIndex = letters.indexOf((answerMatch && answerMatch[1].toUpperCase()) || "A");
    questions.push({
      id: `aps-${match[1]}`,
      prompt,
      options,
      answer: options[answerIndex],
      explanation,
      priority: false,
    });
  }

  return questions;
}

